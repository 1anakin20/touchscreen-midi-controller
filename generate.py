def generate(config_path: str, output_path: str):
    with open(config_path, "r", encoding="utf-8") as f:
        # Strip JS-style // comments (JSON doesn't support them natively)
        raw = f.read()
        raw = re.sub(r"//[^\n]*", "", raw)
        cfg = json.loads(raw)

    cfg["_source_file"] = Path(config_path).name
    validate_config(cfg)

    out = Path(output_path)
    out.parent.mkdir(parents=True, exist_ok=True)

    sketch = (
        gen_header(cfg)
        + gen_touch_defines(cfg)
        + gen_display_defines(cfg)
        + gen_midi_defines(cfg)
        + gen_color_defines(cfg)
        + gen_page_and_control_enums(cfg)
        + gen_control_init(cfg)
        + gen_runtime(cfg)
    )

    with open(out, "w", encoding="utf-8") as f:
        f.write(sketch)

    pages    = cfg["pages"]
    controls = sum(len(p.get("controls", [])) for p in pages)
    print(f"✓  Generated: {output_path}")
    print(f"   {len(pages)} page(s), {controls} control(s)")
    print(f"   Screen rotation: {cfg['device']['screen']['rotation']}")
    print(f"   MIDI channel: {cfg['device']['midi']['channel']}  baud: {cfg['device']['midi']['baud']}")


def main():
    parser = argparse.ArgumentParser(
        description="Generate Arduino firmware from a MIDI controller layout JSON."
    )
    parser.add_argument(
        "--config", "-c",
        required=True,
        help="Path to layout JSON file (e.g. my_layout.json)"
    )
    parser.add_argument(
        "--output", "-o",
        default=None,
        help="Output .ino path. Default: firmware/<config_name>/firmware.ino"
    )
    args = parser.parse_args()

    if not os.path.exists(args.config):
        print(f"Error: config file not found: {args.config}")
        sys.exit(1)

    if args.output:
        output = args.output
    else:
        stem = Path(args.config).stem
        output = f"firmware/{stem}/{stem}.ino"

    generate(args.config, output)


if __name__ == "__main__":
    main()
