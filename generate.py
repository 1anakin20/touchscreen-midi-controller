# ── Helpers ──────────────────────────────────────────────────────────────────

def hex_to_rgb565(hex_color: str) -> str:
    """Convert #RRGGBB to RGB565 hex string for use in Arduino code."""
    hex_color = hex_color.lstrip("#")
    if len(hex_color) != 6:
        raise ValueError(f"Invalid color: #{hex_color}")
    r = int(hex_color[0:2], 16)
    g = int(hex_color[2:4], 16)
    b = int(hex_color[4:6], 16)
    r5 = (r >> 3) & 0x1F
    g6 = (g >> 2) & 0x3F
    b5 = (b >> 3) & 0x1F
    rgb565 = (r5 << 11) | (g6 << 5) | b5
    return f"0x{rgb565:04X}"

def c(cfg: dict, key: str, fallback_hex: str = "#000000") -> str:
    """Look up a color in a control's colors block or return fallback."""
    colors = cfg.get("colors", {})
    return hex_to_rgb565(colors.get(key, fallback_hex))

def theme_color(theme: dict, key: str) -> str:
    return hex_to_rgb565(theme.get(key, "#000000"))

def safe_id(s: str) -> str:
    """Turn an id string into a valid C variable name fragment."""
    return re.sub(r"[^a-zA-Z0-9_]", "_", s)

def midi_channel_0indexed(ch: int) -> int:
    return max(0, min(15, ch - 1))

def validate_config(cfg: dict):
    """Basic validation with clear error messages."""
    errors = []
    if "device" not in cfg:
        errors.append("Missing top-level 'device' block")
    if "theme" not in cfg:
        errors.append("Missing top-level 'theme' block")
    if "pages" not in cfg or not cfg["pages"]:
        errors.append("Missing or empty 'pages' array")

    seen_control_ids = set()
    for pi, page in enumerate(cfg.get("pages", [])):
        if "id" not in page:
            errors.append(f"Page {pi}: missing 'id'")
        if "label" not in page:
            errors.append(f"Page {pi}: missing 'label'")
        for ci, ctrl in enumerate(page.get("controls", [])):
            cid = ctrl.get("id", f"page{pi}_ctrl{ci}")
            if cid in seen_control_ids:
                errors.append(f"Duplicate control id: '{cid}'")
            seen_control_ids.add(cid)
            if "type" not in ctrl:
                errors.append(f"Control '{cid}': missing 'type'")
            elif ctrl["type"] not in ("button", "toggle", "slider"):
                errors.append(f"Control '{cid}': unknown type '{ctrl['type']}'")
            for field in ("x", "y", "w", "h"):
                if field not in ctrl:
                    errors.append(f"Control '{cid}': missing '{field}'")
            if "midi" not in ctrl:
                errors.append(f"Control '{cid}': missing 'midi' block")

    if errors:
        print("Config validation errors:")
        for e in errors:
            print(f"  ✗  {e}")
        sys.exit(1)


